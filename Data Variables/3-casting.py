# Casting helps change the data type to the one you are looking for.

# It can be done with constructor functions:

# Int
x = int(1)   # x will be 1
y = int(2.8)  # y will be 2
z = int("3")  # z will be 3

# Float
a = float(1)     # x will be 1.0
b = float(2.8)   # y will be 2.8
c = float("3")   # z will be 3.0
d = float("4.2")  # w will be 4.2

# Strings1
g = str("s1")  # x will be 's1'
h = str(2)    # y will be '2'
i = str(3.0)  # z will be '3.0'

print(x, y, z)
print(a, b, c, d)
print(g, h, i)
