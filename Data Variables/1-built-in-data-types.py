# Python has the following data types built-in by default, in these categories:

# Text Type: 	    str
str = 'Hola amigos!'

# Numeric Types: 	int, float, complex
int = 10
float = 10.5
complex = 1j

# Sequence Types: 	list, tuple, range
list = ["apple", "pear", "banana"]
tuple = ("apple", "pear", "banana")
range = range(6)

# Mapping Type: 	dict
dict = {"name": "Rama", "age": 26}

# Set Types: 	    set, frozenset
set = {"apple", "pear", "banana"}
frozenset = frozenset({"apple", "pear", "banana"})

# Boolean Type: 	bool
bool = True

# Binary Types: 	bytes, bytearray, memoryview
byte = b"Hello"
bytearray = bytearray(5)
memoryview = memoryview(bytes(5))

# None Type: 	    NoneType
noneType = None

print(str)
print(int, float, complex)
print(list, tuple, range)
print(dict)
print(set, frozenset)
print(bool)
print(byte, bytearray, memoryview)
print(noneType)
