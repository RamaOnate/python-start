# Assigning different values to multiple variables
x, y, z = 'Maple', 'Runescape', 'Idle'
print(x)
print(y)
print(z)


# Assigning the same value to multiple variables
a = b = c = 'Pog'
print(a, b, c)


# Unpacking a collection
vegetables = ["Tomato", "Lettuce", "Pepper"]
t, l, p = vegetables
print(t)
print(l)
print(p)
