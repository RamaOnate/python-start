# To output we use 'print()'

x = 'Hello everyone'
print(x)

# To concat the output of strings...

y = 'how are you'
z = 'all doing this afternoon?'
print(x, y, z)

# Or simply use the + operator

print(x + ' ' + y + ' ' + z)

# You can also print an operation

a = 5
b = 95
print('a + b = ' + str(a + b))
